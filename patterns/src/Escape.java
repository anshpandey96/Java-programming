public class Escape  {

    public static void main(String[] args) {
        System.out.println("Hello  ansh \n pandey");
        System.out.println("Hello  ansh \t pandey");
        System.out.println("Hello  ansh \" pandey");
        System.out.println("Hello  ansh \b pandey");
        System.out.println("Hello  ansh \\ pandey");

    }
}
